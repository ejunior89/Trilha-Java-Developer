public class SistemaMedida {
    public static void main(String[] args) {
        String sigla = "p";

        if(sigla == "p")
        System.out.println("PEQUENO");

        else if(sigla == "M")
        System.out.println("MEDIO");
    
        else if(sigla == "G")
        System.out.print("GRANDE");

        else
        System.out.println("indefinido");
}
}